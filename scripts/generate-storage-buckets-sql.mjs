#!/usr/bin/env node
/**
 * Reads scripts/storage-buckets.manifest.json and writes
 * scripts/021_ensure_all_app_storage_buckets.sql
 *
 * Usage (from repo root or safe-hands):
 *   node scripts/generate-storage-buckets-sql.mjs
 *
 * Add a bucket: edit the manifest, run this script, then run the SQL in Supabase.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const manifestPath = join(__dirname, 'storage-buckets.manifest.json');
const outPath = join(__dirname, '021_ensure_all_app_storage_buckets.sql');

function sqlEscape(str) {
  return String(str).replace(/'/g, "''");
}

function mimeArraySql(types) {
  if (types == null || !Array.isArray(types) || types.length === 0) {
    return 'NULL';
  }
  const parts = types.map((t) => `'${sqlEscape(t)}'`);
  return `ARRAY[${parts.join(', ')}]::text[]`;
}

const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'));
const buckets = manifest.buckets;
if (!Array.isArray(buckets) || buckets.length === 0) {
  console.error('storage-buckets.manifest.json: "buckets" must be a non-empty array');
  process.exit(1);
}

const valueRows = buckets.map((b) => {
  if (!b.id || typeof b.id !== 'string') {
    throw new Error('Each bucket needs a string "id"');
  }
  const id = sqlEscape(b.id);
  const pub = b.public === true;
  const limit =
    typeof b.fileSizeLimitBytes === 'number' && Number.isFinite(b.fileSizeLimitBytes)
      ? b.fileSizeLimitBytes
      : 5242880;
  const mime = mimeArraySql(b.allowedMimeTypes);
  return `  (
    '${id}',
    '${id}',
    ${pub},
    ${limit},
    ${mime}
  )`;
});

const sql = `-- Auto-generated by scripts/generate-storage-buckets-sql.mjs from storage-buckets.manifest.json
-- Do not edit this file by hand; change the manifest and re-run the generator.
--
-- Run once in Supabase SQL Editor (same project as your app .env).
-- Idempotent: safe to re-run; updates public / size limit / MIME allowlist on conflict.
--
-- App references:
--   listing-images   → app/api/listings, app/api/listings/[id]
--   dispute-evidence → lib/evidenceUpload.js
--
-- Next: run 022_storage_objects_policies_safe_hands.sql so storage.objects RLS matches these buckets.

INSERT INTO storage.buckets (id, name, public, file_size_limit, allowed_mime_types)
VALUES
${valueRows.join(',\n')}
ON CONFLICT (id) DO UPDATE
SET
  public = EXCLUDED.public,
  file_size_limit = EXCLUDED.file_size_limit,
  allowed_mime_types = EXCLUDED.allowed_mime_types;

SELECT id, name, public, file_size_limit, allowed_mime_types, created_at
FROM storage.buckets
WHERE id = ANY (ARRAY[${buckets.map((b) => `'${sqlEscape(b.id)}'`).join(', ')}]::text[])
ORDER BY id;
`;

writeFileSync(outPath, sql, 'utf8');
console.error('Wrote', outPath);
